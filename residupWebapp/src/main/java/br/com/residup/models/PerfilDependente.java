package br.com.residup.models;

public class PerfilDependente {

    private int idDependente;
    private String nomeDependente;
    private String documentoDependente;

    public int getIdDependente() {
        return idDependente;
    }

    public void setIdDependente(int idDependente) {
        this.idDependente = idDependente;
    }

    public String getNomeDependente() {
        return nomeDependente;
    }

    public void setNomeDependente(String nomeDependente) {
        this.nomeDependente = nomeDependente;
    }

    public String getDocumentoDependente() {
        return documentoDependente;
    }

    public void setDocumentoDependente(String documentoDependente) {
        this.documentoDependente = documentoDependente;
    }
}
